// Exercício 3 — Contador de 1 a 10

// O loop "for" vai repetir uma ação várias vezes
let i = 1;

/*enquanto o valor de i for menor ou igual a 10 vai repetir o que esta dentro de {} e a cada
repetição vai mostrar o valor de i e vai aumentar i em 1 */
while (i <= 10) {  
  console.log(`Contagem: ${i}`);
  i++;
}

